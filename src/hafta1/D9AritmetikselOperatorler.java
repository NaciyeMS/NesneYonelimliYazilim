package hafta1;

public class D9AritmetikselOperatorler {

	public static void main(String[] args) {
		//TOPLAMA
		int a=5,b=4;
		System.out.print("Toplama Operatörü");
		System.out.println(a+b);
		//BİRLEŞTİRME
		String x="5",y="4";
		System.out.print("Birleştirme Operatörü");
		System.out.println(x+y);
		//ÇIKARMA
		System.out.print("Çıkarma Operatörü");
		System.out.println(a-b);
		//ÇARPMA
		System.out.print("Çarpma Operatörü");
		System.out.println(a*b);
		//BÖLME
		System.out.print("Bölme Operatörü");
		System.out.println(a/b);
		//KALAN
		System.out.print("Kalan Operatörü");
		System.out.println(a%b);
	}
}
