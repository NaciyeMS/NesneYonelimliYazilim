package hafta1;

public class D7ToplamaOperatoru {
	public static void main(String[] args) {
		//+ iki yanına sayı gelirse toplama operatörü olarak çalışır
		int a=4,b=98;
		int toplam=a+b;
		System.out.println("Sonuç:"+toplam);
		System.out.println("Sonuç:"+a+b); //birleştirme operatörü 
		System.out.println("Sonuç:"+(a+b)); //toplama operatörü 
		//() işlem önceliği var
	}

}
